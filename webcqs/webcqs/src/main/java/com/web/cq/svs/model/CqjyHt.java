	public WI toWI(){
		Wto to = new Wto();
		CqjyCrsq cr =null;
		String jc="";
		String dw ="";
		if(gs!=null){
			cr = gs.getCr();
		}else if(xs!=null){
			cr = xs.getCr();
		}
		if(cr!=null){
			WOrgz jyzx=cr.getJyzx();
			if(jyzx!=null)jc = jyzx.getJc();
			if(cr.getDw()!=null)dw = cr.getDw().getJc();
		}
		to.put("xmbh",xmbh!=null ? xmbh : cr!=null ? cr.getXmbh() : null);
		if(xmmc!=null){			
			if(dwmc!=null){
				to.put("xmmc", dwmc+xmmc);
			}			
			to.put("mc", xmmc);
		}else if(cr!=null){	
			String mc = cr.getMc();
			to.put("mc",mc);
			to.put("xmmc",jc+dw+mc);
		}
		if(crdwmc!=null){
			to.put("crfmc",crdwmc);
		}else if(cr!=null){
			to.put("crfmc", jc+dw+cr.getCrfmc());
		}
		to.put("cjrq",cjrq);
		if(jyfs!=null){
			to.put("jyfs",jyfs);
		}if(cr!=null){
			String jyfs = cr.getJyfsText();
			if(jyfs!=null){
				to.put("jyfs",jyfs);
			}else{
				int j = cr.getJyfs();
				switch(j){
				case 1:
					to.put("jyfs","村组织招标");
					break;
				case 2:
					to.put("jyfs","委托中介招标");
					break;
				default:
					to.put("jyfs","公开协商");
					break;
				}
			}
		}
		if(gm!=null){
			to.put("gm",gm);
			if(xmlx==1){
				to.put("gm_unit","平方米");
			}else if(xmlx<=6&&xmlx>=4){
				to.put("gm_unit","亩");
			}
		}else if(cr!=null){
			int lx = cr.getJyxmfl();
			if(1 == lx){
				to.put("gm",cr.getJzmj());
				to.put("gm_unit","平方米");
			}else if(lx <=6 && lx>=4){
				to.put("gm",cr.getMj());
				to.put("gm_unit","亩");
			}
		}
		if(cr!=null){
			to.put("lx",cr.getJyxmfl());
			to.put("lxText",cr.getJyxmflText());
			to.put("jzmj",cr.getJzmj());
			to.put("mj",cr.getMj());
		}
		if(srdw!=null){
			to.put("srfmc",srdw);
		}else if(gs!=null){
			to.put("srfmc", gs.getZbdw());
		}else if(xs!=null){
			to.put("srfmc",xs.getSrf());
		}
		to.put("qsrq",qsrq);
		to.put("zzrq",zzrq);
		to.put("htzje",htzje);
		return to;
	}
